package com.vasudev.may30;

class PositiveOrNegative {

	public static void main(String[] args) {
		
		int a = 0;
		if(a==0) {
			System.out.println("Number "+a+
					" is Zero.");
		}else {
			if(a>0)
				System.out.println("Number "+a+
						" is Positive.");
			else
				System.out.println("Number "+a+
						" is Negative");
		}

	}

}
